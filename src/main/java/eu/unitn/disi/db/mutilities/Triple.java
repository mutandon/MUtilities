package eu.unitn.disi.db.mutilities;

import java.util.Objects;

/**
 * A simple class representing a triple of objects
 * 
 * @author Davide Mottin <mottin@disi.unitn.eu>
 * @param <T1>
 * @param <T2>
 * @param <T3>
 */
public class Triple<T1,T2,T3> {
    private T1 first; 
    private T2 second; 
    private T3 third;

    public Triple() {
        
    }

    public Triple(T1 first, T2 second, T3 third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }
    
    public T1 getFirst() {
        return first;
    }

    public void setFirst(T1 first) {
        this.first = first;
    }

    public T2 getSecond() {
        return second;
    }

    public void setSecond(T2 second) {
        this.second = second;
    }

    public T3 getThird() {
        return third;
    }

    public void setThird(T3 third) {
        this.third = third;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Triple<?, ?, ?> other = (Triple<?, ?, ?>) obj;
        if (!Objects.equals(this.first, other.first)) {
            return false;
        }
        if (!Objects.equals(this.second, other.second)) {
            return false;
        }
        if (!Objects.equals(this.third, other.third)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "<" + first + ", " + second + ", " + third + '>';
    }
}
